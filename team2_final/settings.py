def env_get(key: str, default=None):
    """
    우선순위: OS 환경변수 → .env → .env.prod → default
    (.env.prod는 로컬에서 배포 값 참고용 '백업 소스' 정도로만 사용)
    """
    v = os.getenv(key)
    if v not in (None, ""):
        return v
    v = _FILE_ENV.get(key)
    if v not in (None, ""):
        return v
    v = _FILE_ENV_PROD.get(key)
    if v not in (None, ""):
        return v
    return default


